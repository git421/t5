    private void jRadioButtonDefaultStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jRadioButtonDefaultStateChanged
        if (jRadioButtonDefault.isSelected()) {
            this.jButtonColor.setEnabled(false);
            this.jSpinnerOutline.setEnabled(false);
            this.jCheckBoxFill.setEnabled(false);
        } else {
            this.jButtonColor.setEnabled(true);
            this.jSpinnerOutline.setEnabled(true);
            this.jCheckBoxFill.setEnabled(true);
        }

    }//GEN-LAST:event_jRadioButtonDefaultStateChanged

    private void jButtonColorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonColorActionPerformed
        this.selectedColor = JColorChooser.showDialog(this, "Choose a color for your shape", Color.blue);
        if (selectedColor != null) {
            this.jButtonColor.setBackground(selectedColor);
        }
        if (selectedColor == defaultColor) {
            this.jButtonColor.setBackground(null);
        }
    }//GEN-LAST:event_jButtonColorActionPerformed

