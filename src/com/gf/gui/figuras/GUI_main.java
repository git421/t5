    private void jMenuItemRectanguloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemRectanguloActionPerformed
        String inputW = JOptionPane.showInputDialog("Enter width: ");
        String inputH = JOptionPane.showInputDialog("Enter height: ");
        if ((inputW != null && !inputW.isEmpty())&&(inputH != null && !inputH.isEmpty())) {
             

            try {
                int width = Integer.parseInt(inputW);
                int height = Integer.parseInt(inputH);

                if (width > 0&&height>0) {
                    Rectangulo r = new Rectangulo(width, height, this.selectedColor, this.fill, this.outlineWidth);
                    GUI_canvas canvas = new GUI_canvas(r);
                    canvas.setVisible(true);

                } else {
                    JOptionPane.showMessageDialog(this, "Height or width values not allowed", "H/W Error", JOptionPane.ERROR_MESSAGE);

                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Height or width values not allowed", "H/W Error", JOptionPane.ERROR_MESSAGE);
            }
            

        } else {
            JOptionPane.showMessageDialog(this, "You did not set the height or width values", "H/W Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jMenuItemRectanguloActionPerformed

