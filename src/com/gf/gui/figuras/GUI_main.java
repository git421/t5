    private void jMenuItemCirculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemCirculoActionPerformed
        String input = JOptionPane.showInputDialog("Enter radius: ");
        if (input != null && !input.isEmpty()) {
            //tengo un radio 

            try {
                int radius = Integer.parseInt(input);

                if (radius > 0) {
                    Circulo c = new Circulo(radius, this.selectedColor, this.fill, this.outlineWidth);
                    GUI_canvas canvas = new GUI_canvas(c);
                    canvas.setVisible(true);

                } else {
                    JOptionPane.showMessageDialog(this, "radius value not allowed", "Radius Error", JOptionPane.ERROR_MESSAGE);

                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "radius value not allowed", "Radius Error", JOptionPane.ERROR_MESSAGE);
            }
            //si ha salido bien, radio es numerico

        } else {
            JOptionPane.showMessageDialog(this, "You did not set a radius value", "Radius Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jMenuItemCirculoActionPerformed

